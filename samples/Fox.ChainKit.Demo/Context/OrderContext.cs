//==================================================================================================
// Order context for demo application.
// Represents an order being processed through the chain.
//==================================================================================================

namespace Fox.ChainKit.Demo.Context;

//==================================================================================================
/// <summary>
/// Context object representing an order in the processing pipeline.
/// </summary>
//==================================================================================================
internal sealed class OrderContext
{
    //==============================================================================================
    /// <summary>
    /// Gets or sets the order identifier.
    /// </summary>
    //==============================================================================================
    public string OrderId { get; set; } = string.Empty;

    //==============================================================================================
    /// <summary>
    /// Gets or sets the order amount.
    /// </summary>
    //==============================================================================================
    public decimal Amount { get; set; }

    //==============================================================================================
    /// <summary>
    /// Gets or sets the customer email address.
    /// </summary>
    //==============================================================================================
    public string CustomerEmail { get; set; } = string.Empty;

    //==============================================================================================
    /// <summary>
    /// Gets or sets the processing log entries.
    /// </summary>
    //==============================================================================================
    public List<string> ProcessingLog { get; set; } = [];

    //==============================================================================================
    /// <summary>
    /// Gets or sets a value indicating whether the order is valid.
    /// </summary>
    //==============================================================================================
    public bool IsValid { get; set; } = true;
}
