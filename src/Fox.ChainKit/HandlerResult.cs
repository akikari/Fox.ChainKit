//==================================================================================================
// Represents the result of a handler execution in the chain.
// Indicates whether the chain should continue or stop after this handler.
//==================================================================================================

namespace Fox.ChainKit;

//==================================================================================================
/// <summary>
/// Represents the result of a handler execution.
/// </summary>
//==================================================================================================
public enum HandlerResult
{
    //==============================================================================================
    /// <summary>
    /// Continue executing the next handler in the chain.
    /// </summary>
    //==============================================================================================
    Continue,

    //==============================================================================================
    /// <summary>
    /// Stop the chain execution immediately. No further handlers will be executed.
    /// </summary>
    //==============================================================================================
    Stop
}
